package hw9.phonebook;

public class Cell {
    private String name  ;
    private int phone ;

    public Cell() {
    }

    public Cell(String name, int phone) {
        this.name = name;
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }
}
